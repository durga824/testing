package com.pojo;

public class Clients {

    String count;
    String chargebee_status;
    String chargebee_plans_count;
    String clients;
    String add_client_permission;

    public String getCount() {
        return count;
    }

    public void setCount(String count) {
        this.count = count;
    }

    public String getChargebee_status() {
        return chargebee_status;
    }

    public void setChargebee_status(String chargebee_status) {
        this.chargebee_status = chargebee_status;
    }

    public String getChargebee_plans_count() {
        return chargebee_plans_count;
    }

    public void setChargebee_plans_count(String chargebee_plans_count) {
        this.chargebee_plans_count = chargebee_plans_count;
    }

    public String getClients() {
        return clients;
    }

    public void setClients(String clients) {
        this.clients = clients;
    }

    public String getAdd_client_permission() {
        return add_client_permission;
    }

    public void setAdd_client_permission(String add_client_permission) {
        this.add_client_permission = add_client_permission;
    }
}
